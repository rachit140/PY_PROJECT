year = int(input("Enter Number of Years"))
days = (year*365)
hours = (year*8766)
minutes = (year*525600)
seconds = (year*31557600)
print ("NO OF DAYS IN YEAR:",days)
print ("NO OF HOURS IN YEAR:",hours)
print ("NO OF MINUTES IN YEAR:",minutes)
print ("NO OF SECONDS IN YEAR:",seconds)
